# Copy the trino bundle at run time so that locally built bundle overrides the one that is present in the image
echo "Copying trino bundle to ${TRINO_HOME}/plugin/hive/"
cp ${HUDI_TRINO_BUNDLE} ${TRINO_HOME}/plugin/hive/

# Add name, value and path into Hadoop config format
function addProperty() {
  local path=$1
  local name=$2
  local value=$3

  local entry="<property><name>$name</name><value>${value}</value></property>"
  local escapedEntry=$(echo $entry | sed 's/\//\\\//g')
  sed -i "/<\/configuration>/ s/.*/${escapedEntry}\n&/" $path
}

# This will transform hadoop.env format into iterable data format that can added to addProperty
function configure() {
    local path=$1
    local module=$2
    local envPrefix=$3

    local var
    local value

    echo "Configuring $module"
    for c in `printenv | perl -sne 'print "$1 " if m/^${envPrefix}_(.+?)=.*/' -- -envPrefix=$envPrefix`; do
        name=`echo ${c} | perl -pe 's/___/-/g; s/__/@/g; s/_/./g; s/@/_/g;'`
        var="${envPrefix}_${c}"
        value=${!var}
        echo " - Setting $name=$value"
        addProperty $path $name "$value"
    done
}

# call configure function with respective parameters
configure /etc/hadoop/core-site.xml core CORE_CONF

/usr/local/trino/bin/launcher run
